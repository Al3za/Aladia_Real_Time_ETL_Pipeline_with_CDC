# Leggere dal topic di kafka. ricorda che i topik sono i messaggi CDC salvati qui in kafka
df = spark.readStream \
    .format("kafka") \
    .option("kafka.bootstrap.servers", "localhost:29092") \
    .option("subscribe", "cdc.public.Employee") \
    .option("startingOffsets", "earliest") \
    .load()

#  HERE STARTS ETL: 
from pyspark.sql.functions import from_json, col, to_timestamp, expr
from pyspark.sql.types import StructType, StructField, StringType, LongType

# Estarai solo i dati che ci servono da payload
schema = StructType([
    StructField("payload", StructType([ # res #payload" (nel message kafka)
        StructField("op", StringType(), True), # in op is store: insert/update/delete operation type in topyc generated by Debezium
        StructField("after", StructType([ # extraxt "after":values(the latest insert, stored in "payload")
            StructField("id", LongType()),
            StructField("email", StringType()),
            StructField("name", StringType()),
            StructField("createdAt", LongType()),
            StructField("updatedAt", LongType()),
            StructField("role", StringType()),
            StructField("password", StringType())
        ]))
    ]))
])

# parse the "payload" message data stored in kafka.

df_parsed = (
    df.selectExpr("CAST(value AS STRING) as json_str")
    #   .select(from_json("json_str", schema).alias("data"))
      .select(from_json("json_str", schema, {"mode": "PERMISSIVE", "columnNameOfCorruptRecord": "_corrupt_record"}).alias("data")) 
      .select(
          col("data.payload.op").alias("op"), # qui attingiamo il campo "op" di debezium (insert/update/delete)
          col("data.payload.after.*"), # # valori post-update/insert (di kafka)
          col("data.payload.before.*"), # valori pre-update/delete (utile per delete in questo esempio)
          col("data._corrupt_record")  # qui finiranno i record json malformati
      )
) #"PERMISSIVE": Spark salva i record malformati in _corrupt_record invece di far crashare il job.


valid_df = df_parsed.filter(
    (col("role").isin("ADMIN", "ENGINEER","INTERN")) &   # role valido
    (col("email").isNotNull()) &                # email non nulla
    (col("createdAt") > 0)                      # createdAt valido
)

df_clean = ( # working on parquet sink
    valid_df # se abbiamo un error qui, andranno in badRecordsPath (/tmp/badRecords)
    
      # CDC semantics
      .filter(col("op").isin("c", "u")) # solo insert/update

      # business rule
      .filter(col("role") == "ADMIN") # logica solo per admin, non per ENGINEER o INTERN

      # Scegli una colonna temporale
      .withColumn(
          "updated_ts",
          to_timestamp(col("updatedAt") / 1000)
      )

       # Applica watermark + deduplication:
      .withWatermark("updated_ts", "10 minutes")
      .dropDuplicates(["id", "updated_ts"]) # quando facciamo un update, l'id gia esistente viene inserito nei topic cafka, ma con updatedAt diverso. Questo assicura che un update non venga eliminato, e salvato correttamente nel sink/db
      # ðŸ“Œ Cosa significa


 ) # in questa "pipeline" df_clean facciamo data transf, e data deduplication


# Gestire delete (on parquet sink)
df_delete = df_parsed.filter(col("op") == "d").select("before.id")


query = df_clean.writeStream \
    .format("parquet") \
    .outputMode("append") \
    .option("checkpointLocation", "/tmp_spark/checkpoint") \
    .option("badRecordsPath", "/tmp/badRecords") \
    .start()


query.awaitTermination()
